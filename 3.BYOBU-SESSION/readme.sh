Byobu:
 byobu new -s any-name
 ctl+a+d - to detach from byobu
 crl+d - to delete byobu
 ctl+a+c - create new terminal in byobu
 ctl+a+p   - previous terminal tab 
 ctl+a+m - next terminal tab

To search in terminal: 
Alt+ pgup
Q - quit


#split screen
shift + f2 - split screen horizontally
ctrl  + f2 - split screen vertically
ctrl  + d  - delete screen
ctrl  + a + tab - to switch between screen

